import re

def split_job_description(job_desc):
    
    # 方式一: 查找第一个“要求”或“资格”，并按此分割
    match = re.search(r"(要求|资格)", job_desc)
    qualifications_1 = ""
    if match:
        # 获取分隔点
        split_pos = match.start()  # 第一个出现的“要求”或“资格”的位置
        
        # 将文本分为两部分
        job_content_1 = job_desc[:split_pos].strip()  # 工作内容
        qualifications_1 = job_desc[split_pos:].strip()  # 任职要求

    # 如果方式一分割的后段长度小于10个汉字，则采用方式二进行分割
    if len(qualifications_1) < 10:
        # 方式二: 在第二个出现的"1"之前进行分割
        match = re.findall(r"1", job_desc)
        if len(match) > 1:
            second_1_position = job_desc.find("1", job_desc.find("1") + 1)
            job_content_2 = job_desc[:second_1_position].strip()
            qualifications_1 = job_desc[second_1_position:].strip()
            
    return qualifications_1 if len(qualifications_1) > 10 else job_desc

